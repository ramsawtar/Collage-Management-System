    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // Search student by roll number
        String studentRollno = Rollnoo.getText().trim();
        
        if(studentRollno.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter Roll Number");
            return;
        }
        
        // Validate input
        try {
            int rollNo = Integer.parseInt(studentRollno);
            loadStudentsData(rollNo);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid numeric Roll Number");
        }
    }
    
    // Helper method to load student data (for both load all and search)
    private void loadStudentsData(Integer rollNo) {
        Connection con = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;
        
        try {
            con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
            
            String query;
            if (rollNo == null) {
                // Load all students
                query = "SELECT * FROM student ORDER BY Rollno";
                pstmt = con.prepareStatement(query);
            } else {
                // Search specific student
                query = "SELECT * FROM student WHERE Rollno = ?";
                pstmt = con.prepareStatement(query);
                pstmt.setInt(1, rollNo);
            }
            
            rs = pstmt.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            model.setRowCount(0); // Clear existing rows
            
            boolean found = false;
            while(rs.next()){
                found = true;
                int sRollno = rs.getInt("Rollno");
                String studentrollno = Integer.toString(sRollno);
                String studentname = rs.getString("Name");
                String studentclass = rs.getString("Class");
                Object[] row = {studentrollno, studentname, studentclass};
                model.addRow(row);
            }
            
            if (!found && rollNo != null) {
                JOptionPane.showMessageDialog(this, "Student with Roll Number " + rollNo + " not found!");
            }
            
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
            e.printStackTrace();
        } finally {
            // Close resources in finally block
            try {
                if (rs != null) rs.close();
                if (pstmt != null) pstmt.close();
                if (con != null) con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

