        // TODO add your handling code here:
        Connection con = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;
    
    try {
        // For MySQL 8.x, use this connection URL
        String url = "jdbc:mysql://localhost:3306/startersql?useSSL=false&serverTimezone=UTC";
        con = DriverManager.getConnection(url, "root", "password");
        
        String query = "SELECT * FROM student";
        pstmt = con.prepareStatement(query);
        rs = pstmt.executeQuery();
        
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.setRowCount(0); // Clear existing rows
        
        while(rs.next()){
            int sRollno = rs.getInt("Rollno");
            String studentrollno = Integer.toString(sRollno);
            String studentname = rs.getString("Name");
            String studentclass = rs.getString("Class");
            String row[] = {studentrollno, studentname, studentclass};
            model.addRow(row);
        }
        
    } catch(SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        e.printStackTrace();
    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    } finally {
        // Close resources in finally block
        try {
            if (rs != null) rs.close();
            if (pstmt != null) pstmt.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
